const prompt = require('prompt-sync')({sigint: true})

let num1 = Number(prompt(" Enter a number from 1 - 7 to find out what's your favorite day of the week :"))

if (num1 = 1){
    console.log("Monday")
}
if (num1 = 2){
    console.log("Tuesday")
}
if (num1 = 3){
    console.log("Wedensday")
}

if(num1 = 4){
    console.log("Thursday")
}
if (num1 = 5){
    console.log("Friday")
}
if (num1 = 6){
    console.log("Saturday")
}
if(num1 = 7){
    console.log("Sunday")
}
else {
    console.log ("Error")
}
